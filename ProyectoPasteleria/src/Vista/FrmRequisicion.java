    private void ButtonEliminarActionPerformedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEliminarActionPerformedActionPerformed
        // TODO add your handling code here:
       if(txtNoRequisicion.getText().equals("")){

        JOptionPane.showMessageDialog(this,"Selecciona una requisición");

        return;

        }

        int confirmar =JOptionPane.showConfirmDialog( this,
                "¿Deseas eliminar esta requisición?", "Confirmar",JOptionPane.YES_NO_OPTION);

        if(confirmar != JOptionPane.YES_OPTION){

            return;

        }

        RequisicionDAO dao = new RequisicionDAO();

        boolean respuesta = dao.eliminarRequisicion(Integer.parseInt(txtNoRequisicion.getText()));

        if(respuesta){

            JOptionPane.showMessageDialog(this,"Requisición eliminada");

            dao.mostrarRequisiciones(tblRequisiciones);

            limpiar();

        }else{

            JOptionPane.showMessageDialog(this,"Error al eliminar");

        }

    }//GEN-LAST:event_ButtonEliminarActionPerformedActionPerformed

