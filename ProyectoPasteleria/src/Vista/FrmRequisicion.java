    private void BtnGuardarReq(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarReq
        // TODO add your handling code here:
       try{

        Requisicion req = new Requisicion();

        String departamento =
                cmbDepartamento.getSelectedItem().toString();

        RequisicionDAO dao =
                new RequisicionDAO();

        int idDepartamento =
                dao.obtenerIdDepartamento(departamento);

        req.setIdDepartamento(idDepartamento);

        SimpleDateFormat formato =
                new SimpleDateFormat("yyyy-MM-dd");

        req.setFecha(
                formato.parse(
                        txtFechaReq.getText()));

        String pedido =
                cmbNoPedidos.getSelectedItem().toString();

        int idPedido =
                Integer.parseInt(
                        pedido.split(" - ")[0]);

        req.setIdPedido(idPedido);

        req.setSolicitante(
                cmbSolicitante.getSelectedItem().toString());

        req.setObservaciones(
                txtObservaciones.getText());

        req.setTotal(
                Double.parseDouble(
                        txtTotalReq.getText()));

        req.setEntregadoPor(
                txtEntrega.getText());

        req.setRecibidoPor(
                txtRecibido.getText());

        // VALIDAR INVENTARIO ANTES DE GUARDAR

        DefaultTableModel modelo =
                (DefaultTableModel)
                tblMaterialesSolicitados.getModel();

            for(int i = 0;
                i < modelo.getRowCount();
                i++){

                String ingrediente =
                        modelo.getValueAt(i, 1)
                        .toString();

                int idIngrediente =
                        dao.obtenerIdIngrediente(
                                ingrediente);

                double cantidad =
                        Double.parseDouble(
                                modelo.getValueAt(i, 3)
                                .toString());

                if(!dao.verificarExistencia(
                        idIngrediente,
                        cantidad)){

                    JOptionPane.showMessageDialog(
                            this,
                            "Inventario insuficiente para: "
                            + ingrediente);

                    return;
                }

            }

            // GUARDAR REQUISICIÓN

            int idRequisicion =
                    dao.guardarRequisicion(req);

            if(idRequisicion > 0){

                // GUARDAR DETALLE Y DESCONTAR INVENTARIO

                for(int i = 0;
                    i < modelo.getRowCount();
                    i++){

                    String ingrediente =
                            modelo.getValueAt(i, 1)
                            .toString();

                    int idIngrediente =
                            dao.obtenerIdIngrediente(
                                    ingrediente);

                    double cantidad =
                            Double.parseDouble(
                                    modelo.getValueAt(i, 3)
                                    .toString());

                    double costoUnitario =
                            Double.parseDouble(
                                    modelo.getValueAt(i, 4)
                                    .toString());

                    double total =
                            Double.parseDouble(
                                    modelo.getValueAt(i, 5)
                                    .toString());

                    dao.guardarDetalleRequisicion(
                            idRequisicion,
                            idIngrediente,
                            cantidad,
                            costoUnitario,
                            total);

                    dao.descontarInventario(
                            idIngrediente,
                            cantidad);

                }

                JOptionPane.showMessageDialog(
                        this,
                        "Requisición guardada correctamente");

                dao.mostrarRequisiciones(
                        tblRequisiciones);

            }else{

            JOptionPane.showMessageDialog(this,"Error al guardar la requisición");

            }

        }catch(Exception e){

        System.out.println("Error: " + e);

        }
        
    }//GEN-LAST:event_BtnGuardarReq

