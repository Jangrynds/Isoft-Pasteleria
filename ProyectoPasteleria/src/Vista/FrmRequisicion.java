    private void ImprimirReqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ImprimirReqActionPerformed
        // TODO add your handling code here:
        if(txtNoRequisicion.getText().equals("")){

            JOptionPane.showMessageDialog( this, "Selecciona una requisición");

            return;
        }
        
        JTextArea reporte = new JTextArea();
        
        reporte.append("PASTELERIA\n\n");

        reporte.append("REQUISICION DE MATERIALES\n\n");

        reporte.append("No. Requisicion: " + txtNoRequisicion.getText() + "\n");

        reporte.append("Fecha: " + txtFechaReq.getText() + "\n\n");

        reporte.append("Departamento: " + cmbDepartamento.getSelectedItem() + "\n");

        reporte.append("Solicitante: " + cmbSolicitante.getSelectedItem() + "\n\n");
        
        DefaultTableModel modelo =
        (DefaultTableModel)
        tblMaterialesSolicitados.getModel();

        reporte.append("Cant.    Ingrediente         C.Unit.     Total\n");

        reporte.append("------------------------------------------------\n");

        for(int i = 0;
            i < modelo.getRowCount();
            i++){

            reporte.append(
                modelo.getValueAt(i, 3) + "\t"
                + modelo.getValueAt(i, 1) + "\t"
                + modelo.getValueAt(i, 4) + "\t"
                + modelo.getValueAt(i, 5)
                + "\n");

        }
        
        reporte.append(
        "\n---------------------------------\n");

        reporte.append("TOTAL: $"  + txtTotalReq.getText() + "\n\n");

        reporte.append( "Entregado por: " + txtEntrega.getText() + "\n");

        reporte.append( "Recibido por: " + txtRecibido.getText() + "\n");
        
        try{

            reporte.print();

        }catch(Exception e){

            System.out.println(
                    "Error: " + e);

        }
    }//GEN-LAST:event_ImprimirReqActionPerformed

