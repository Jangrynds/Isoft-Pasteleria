    private void tblEmpleadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmpleadoMouseClicked
        // TODO add your handling code here:
        int fila = tblEmpleado.getSelectedRow();

        if(fila >= 0){

            txtIdEmpleado.setText(
                tblEmpleado.getValueAt(fila, 0).toString()
            );

            txtNombre.setText(
                tblEmpleado.getValueAt(fila, 1).toString()
            );

            txtApellidoP.setText(
                tblEmpleado.getValueAt(fila, 2).toString()
            );

            txtApellidoM.setText(
                tblEmpleado.getValueAt(fila, 3).toString()
            );

            txtTelefono.setText(
                tblEmpleado.getValueAt(fila, 4).toString()
            );
            
            cmbDepartamento.setSelectedItem(
            tblEmpleado.getValueAt(fila, 5).toString());
            
            txtSalarioHora.setText(
                tblEmpleado.getValueAt(fila, 6).toString());

        }
    }//GEN-LAST:event_tblEmpleadoMouseClicked

    private void buttonEditarActionPerformedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEditarActionPerformedActionPerformed
        // TODO add your handling code here:
        
        if(txtIdEmpleado.getText().equals("")){

            JOptionPane.showMessageDialog(this,
                    "Selecciona un empleado");

            return;

        }
        Empleado emp = new Empleado();

        emp.setIdEmpleado(
            Integer.parseInt(txtIdEmpleado.getText()));

        emp.setNombre(txtNombre.getText());

        emp.setApellidoPaterno(txtApellidoP.getText());

        emp.setApellidoMaterno(txtApellidoM.getText());

        emp.setTelefono(txtTelefono.getText());

        int idDepartamento =
                cmbDepartamento.getSelectedIndex() + 1;
        
        emp.setSalarioHora(
            Double.parseDouble(txtSalarioHora.getText()));
        

        emp.setIdDepartamento(idDepartamento);

        EmpleadoDAO dao = new EmpleadoDAO();

        boolean respuesta = dao.editarEmpleado(emp);

        if(respuesta){

            JOptionPane.showMessageDialog(this,
                    "Empleado actualizado");

            dao.mostrarEmpleados(tblEmpleado);

        }else{

            JOptionPane.showMessageDialog(this,
                    "Error al actualizar");

        }
        limpiar();
    }//GEN-LAST:event_buttonEditarActionPerformedActionPerformed

    private void ButtonEliminarActionPerformedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEliminarActionPerformedActionPerformed
        // TODO add your handling code here:
        int opcion = JOptionPane.showConfirmDialog(
            this,
            "¿Deseas eliminar este empleado?",
            "Confirmar",
            JOptionPane.YES_NO_OPTION);

        if(opcion == JOptionPane.YES_OPTION){

            int id = Integer.parseInt(
                    txtIdEmpleado.getText());

            EmpleadoDAO dao = new EmpleadoDAO();

            boolean respuesta = dao.eliminarEmpleado(id);

            if(respuesta){

                JOptionPane.showMessageDialog(this,
                        "Empleado eliminado");

                dao.mostrarEmpleados(tblEmpleado);

            }else{

                JOptionPane.showMessageDialog(this,
                        "Error al eliminar");

            }

        }
        limpiar();
    }//GEN-LAST:event_ButtonEliminarActionPerformedActionPerformed

