    private void jButtonGuardarEmpleado(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarEmpleado
        // TODO add your handling code here:
        // Crear objeto empleado
    Empleado emp = new Empleado();

    // Obtener datos de los JTextField
    emp.setNombre(txtNombre.getText());

    emp.setApellidoPaterno(txtApellidoP.getText());

    emp.setApellidoMaterno(txtApellidoM.getText());

    emp.setTelefono(txtTelefono.getText());
    
    int idDepartamento = cmbDepartamento.getSelectedIndex() + 1;

    emp.setIdDepartamento(idDepartamento);
    
    emp.setContrasena(txtSalarioHora.getText());
    
    emp.setSalarioHora(Double.parseDouble(txtSalarioHora.getText()));

    // Crear DAO
    EmpleadoDAO dao = new EmpleadoDAO();

    // Guardar
    boolean respuesta = dao.guardarEmpleado(emp);

        if(respuesta){

            JOptionPane.showMessageDialog(this,
                "Empleado guardado correctamente");

        }else{

            JOptionPane.showMessageDialog(this,
                "Error al guardar");

        }
        //RECARGAR TABLA
        dao.mostrarEmpleados(tblEmpleado);

        //LIMPIAR CAMPOS
        limpiar();
    }//GEN-LAST:event_jButtonGuardarEmpleado

