    private void jButtonGuardarProducto(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarProducto
        // TODO add your handling code here:
        
        if(txtNombreProducto.getText().equals("") ||
            txtPrecioVenta.getText().equals("")){

            JOptionPane.showMessageDialog(this,
                 "Complete los campos obligatorios");

            return;
         }
        // Crear objeto Producto
        Producto prod = new Producto();

        prod.setNombreProducto(
                txtNombreProducto.getText());

        // Categoría
        String categoriaSeleccionada =
                cmbCategoria.getSelectedItem().toString();

        CategoriaProductoDAO catDAO =
                new CategoriaProductoDAO();

        prod.setIdCategoria(
                catDAO.obtenerIdCategoria(
                        categoriaSeleccionada));

        // Tamaño
        prod.setTamano(
                cmbTamano.getSelectedItem().toString());

        // Descripción
        prod.setDescripcion(
                txtDescripcion.getText());

        // Precio
        prod.setPrecioVenta(
                Double.parseDouble(
                        txtPrecioVenta.getText()));

        // Estado
        String estadoSeleccionado =
                cmbEstado.getSelectedItem().toString();

        EstadoProductoDAO estDAO =
                new EstadoProductoDAO();

        prod.setIdEstadoProducto(
                estDAO.obtenerIdEstado(
                        estadoSeleccionado));

        // Guardar
        ProductoDAO dao = new ProductoDAO();

        dao.guardarProducto(prod);
        
         dao.mostrarProductos(tblProducto);
        
        //Limpiar campos
        limpiar();
    }//GEN-LAST:event_jButtonGuardarProducto

    private void buttonEditarActionPerformedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEditarActionPerformedActionPerformed
        // TODO add your handling code here:
        if(txtIdProducto.getText().equals("")){

            JOptionPane.showMessageDialog(this,
                    "Selecciona un empleado");

            return;

        }
        Producto prod = new Producto();

        prod.setIdProducto(
            Integer.parseInt(txtIdProducto.getText())
        );

        prod.setNombreProducto(txtNombreProducto.getText());

        int idCategoria =
                cmbCategoria.getSelectedIndex();
        prod.setIdCategoria(idCategoria);

        prod.setDescripcion(txtDescripcion.getText());

        int idEstado =
            cmbEstado.getSelectedIndex();

        prod.setIdEstadoProducto(idEstado);

        prod.setPrecioVenta(
                Double.parseDouble(txtPrecioVenta.getText()));
        
        prod.setTamano(
            cmbTamano.getSelectedItem().toString()
        );

        ProductoDAO dao = new ProductoDAO();

        boolean respuesta = dao.editarProducto(prod);

        if(respuesta){

            JOptionPane.showMessageDialog(this,
                    "Producto actualizado");

            dao.mostrarProductos(tblProducto);

        }else{

            JOptionPane.showMessageDialog(this,
                    "Error al actualizar");

        }
        limpiar();
    }//GEN-LAST:event_buttonEditarActionPerformedActionPerformed

    private void ButtonEliminarActionPerformedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEliminarActionPerformedActionPerformed
        // TODO add your handling code here:
        int opcion = JOptionPane.showConfirmDialog(
            this,
            "¿Deseas eliminar este producto?",
            "Confirmar",
            JOptionPane.YES_NO_OPTION);

        if(opcion == JOptionPane.YES_OPTION){

            int id = Integer.parseInt(
                    txtIdProducto.getText());

            ProductoDAO dao = new ProductoDAO();

            boolean respuesta = dao.eliminarProducto(id);

            if(respuesta){

                JOptionPane.showMessageDialog(this,
                        "Producto eliminado");

                dao.mostrarProductos(tblProducto);

            }else{

                JOptionPane.showMessageDialog(this,
                        "Error al eliminar");

            }

        }
        limpiar();
    }//GEN-LAST:event_ButtonEliminarActionPerformedActionPerformed

