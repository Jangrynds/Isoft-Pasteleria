    private void tblProductoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductoMouseClicked
        // TODO add your handling code here:
        int fila = tblProducto.getSelectedRow();

        if(fila >= 0){

            txtIdProducto.setText(
                tblProducto.getValueAt(fila, 0).toString()
            );

            txtNombreProducto.setText(
                tblProducto.getValueAt(fila, 1).toString()
            );
            
            cmbCategoria.setSelectedItem(
                tblProducto.getValueAt(fila, 2).toString()
            );
            
            cmbTamano.setSelectedItem(
                tblProducto.getValueAt(fila, 3).toString()
            );
             
            txtDescripcion.setText(
                tblProducto.getValueAt(fila, 4).toString()
            );
            
            txtPrecioVenta.setText(
                tblProducto.getValueAt(fila, 5).toString()
            );
            
            cmbEstado.setSelectedItem(
                tblProducto.getValueAt(fila, 6).toString()
            );
        }
    }//GEN-LAST:event_tblProductoMouseClicked

