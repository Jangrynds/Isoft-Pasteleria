    private void AgregarTiempoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarTiempoActionPerformed
        // TODO add your handling code here:
        try{

            LocalTime inicio = LocalTime.parse(txtHoraInicio.getText());

            LocalTime fin = LocalTime.parse(txtHoraFin.getText());

            Duration duracion = Duration.between(inicio, fin);

            double horas = duracion.toMinutes() / 60.0;

            txtTiempoTotal.setText( String.valueOf(horas));

            double tasaHora = Double.parseDouble(txtTasaHora.getText());

            double costo = horas * tasaHora;

            txtCostoTotal.setText(String.format("%.2f", costo));

            DefaultTableModel modelo =(DefaultTableModel)tblTiempos.getModel();

            modelo.addRow(new Object[]{

                modelo.getRowCount() + 1,
                txtFechaTarjeta.getText(),
                txtHoraInicio.getText(),
                txtHoraFin.getText(),
                horas,
                tasaHora,
                String.format("%.2f", costo)

            });
            
            double totalHoras = 0;
            double totalCosto = 0;

            for(int i = 0;
                i < modelo.getRowCount();
                i++){

                totalHoras += Double.parseDouble(
                        modelo.getValueAt(i, 4)
                        .toString());

                totalCosto += Double.parseDouble(
                        modelo.getValueAt(i, 6)
                        .toString());

            }

            txtTotalHoras.setText(
                    String.format("%.2f",
                            totalHoras));

            txtTotalCosto.setText(
                    String.format("%.2f",
                            totalCosto));

        }catch(Exception e){

            JOptionPane.showMessageDialog(this,
                    "Verifica las horas.\n" + "Formato correcto: HH:mm");

            System.out.println("Error: " + e);
        }
    
        
    }//GEN-LAST:event_AgregarTiempoActionPerformed

    private void BtnGuardarTarjetajButtonGuardarEmpleado(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarTarjetajButtonGuardarEmpleado
        // TODO add your handling code here:
        try{

            TarjetaTiempo tarjeta = new TarjetaTiempo();

            TarjetaTiempoDAO dao = new TarjetaTiempoDAO();

            // ID EMPLEADO

            tarjeta.setIdEmpleado(
                    Integer.parseInt(
                            txtIdEmpleado.getText()));

            // ID PEDIDO

            String pedido =
                    cmbNoPedido
                    .getSelectedItem()
                    .toString();

            int idPedido =
                    Integer.parseInt(
                            pedido.split(" - ")[0]);

            tarjeta.setIdPedido(
                    idPedido);

            // FECHA

            SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");

            tarjeta.setFecha(formato.parse(txtFechaTarjeta.getText()));

            // OBSERVACIONES

            tarjeta.setObservaciones(txtDescripcion.getText());

            // TOTALES

            tarjeta.setTotalHoras(Double.parseDouble(txtTotalHoras.getText()));

            tarjeta.setTotalCosto(Double.parseDouble(txtTotalCosto.getText()));

            // GUARDAR ENCABEZADO

            int idTarjeta =dao.guardarTarjetaTiempo(tarjeta);

            if(idTarjeta > 0){

                DefaultTableModel modelo = (DefaultTableModel)tblTiempos.getModel();

                for(int i = 0; i < modelo.getRowCount(); i++){

                    String dia =modelo.getValueAt(i,1).toString();

                    String horaInicio = modelo.getValueAt(i,2).toString();

                    String horaFin = modelo.getValueAt(i,3).toString();

                    double tiempoTotal = Double.parseDouble(modelo.getValueAt(i,4).toString());

                    double tasaHora = Double.parseDouble(modelo.getValueAt(i,5).toString());

                    double costoTotal = Double.parseDouble(modelo.getValueAt(i,6).toString());

                    dao.guardarDetalleTiempo(idTarjeta,dia,horaInicio,horaFin,tiempoTotal,tasaHora,costoTotal);

                }

                JOptionPane.showMessageDialog(this,"Tarjeta de tiempo guardada correctamente");
                dao.mostrarTarjetas(tblTarjetaTiempo);

            }else{

                JOptionPane.showMessageDialog(this,"Error al guardar");

            }

        }catch(Exception e){

            System.out.println("Error: " + e);

        }

    }//GEN-LAST:event_BtnGuardarTarjetajButtonGuardarEmpleado

