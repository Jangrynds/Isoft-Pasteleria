    private void BtnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnImprimirActionPerformed
        // TODO add your handling code here:
        try{

            JTextArea area =  new JTextArea();

            area.append(
                    "******** TARJETA DE TIEMPO ********\n\n");

            area.append(
                    "No. Tarjeta: "
                    + txtNoTarjeta.getText()
                    + "\n");

            area.append(
                    "Empleado: "
                    + cmbEmpleado.getSelectedItem()
                    + "\n");

            area.append(
                    "No. Nomina: "
                    + txtIdEmpleado.getText()
                    + "\n");

            area.append(
                    "Pedido: "
                    + cmbNoPedido.getSelectedItem()
                    + "\n");

            area.append(
                    "Fecha: "
                    + txtFechaTarjeta.getText()
                    + "\n\n");

            area.append(
                    "Observaciones: "
                    + txtDescripcion.getText()
                    + "\n\n");

            area.append(
                    "------------------------------------------------------------\n");

            area.append(
                    String.format(
                            "%-12s %-10s %-10s %-10s %-10s %-10s\n",
                            "Dia",
                            "Inicio",
                            "Fin",
                            "Horas",
                            "Tasa",
                            "Costo"));

            area.append(
                    "------------------------------------------------------------\n");

            DefaultTableModel modelo =
                    (DefaultTableModel)
                    tblTiempos.getModel();

            for(int i = 0;
                i < modelo.getRowCount();
                i++){

                area.append(
                        String.format(
                                "%-12s %-10s %-10s %-10s %-10s %-10s\n",

                                modelo.getValueAt(i,1),
                                modelo.getValueAt(i,2),
                                modelo.getValueAt(i,3),
                                modelo.getValueAt(i,4),
                                modelo.getValueAt(i,5),
                                modelo.getValueAt(i,6)));

            }

            area.append(
                    "\n------------------------------------------------------------\n");

            area.append(
                    "Total Horas: "
                    + txtTotalHoras.getText()
                    + "\n");

            area.append(
                    "Total Costo: $"
                    + txtTotalCosto.getText()
                    + "\n");

            area.print();

        }catch(Exception e){

            JOptionPane.showMessageDialog(
                    this,
                    "Error al imprimir: "
                    + e.getMessage());

        }
    }//GEN-LAST:event_BtnImprimirActionPerformed

