    private void btnGuardarIngredienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarIngredienteActionPerformed
        // TODO add your handling code here:
        Ingrediente ing = new Ingrediente();

        // Obtener datos de los JTextField
        ing.setNombreIngrediente(txtNombreIngrediente.getText());

        try {
            ing.setCantidad(Integer.parseInt(txtCantidadIngrediente.getText()));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,
                    "Ingrese una cantidad válida");
        }

        String medida = JcomboMedidasIngredientes.getSelectedItem().toString();
        ing.setMedida(medida);

        try {
            ing.setPrecioUnitario(Integer.parseInt(txtPrecioUnitario.getText()));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,
                    "Ingrese una cantidad válida");
        }

        // Crear DAO
        IngredienteDAO dao = new IngredienteDAO();

        // Guardar
        boolean respuesta = dao.guardarIngrediente(ing);

            if(respuesta){

                JOptionPane.showMessageDialog(this,
                    "Ingrediente guardado correctamente");

            }else{

                JOptionPane.showMessageDialog(this,
                    "Error al guardar");

            }
            //RECARGAR TABLA
            dao.mostrarIngredientes(tblIngredientes);

            //LIMPIAR CAMPOS
            limpiar();
    }//GEN-LAST:event_btnGuardarIngredienteActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        if(txtIdIngrediente.getText().equals("")){

            JOptionPane.showMessageDialog(this,
                    "Selecciona un empleado");

            return;

        }
        Ingrediente ing = new Ingrediente();

        ing.setIdIngrediente(
            Integer.parseInt(txtIdIngrediente.getText())
        );

        ing.setNombreIngrediente(txtNombreIngrediente.getText());

        ing.setCantidad(
            Integer.parseInt(txtCantidadIngrediente.getText())
        );

        //Guarda el texto seleccionado en el combobox medidaIngrediente
        String medida = JcomboMedidasIngredientes.getSelectedItem().toString();
        ing.setMedida(medida);
        
        ing.setPrecioUnitario(
            Double.parseDouble(txtPrecioUnitario.getText())
        );

        IngredienteDAO dao = new IngredienteDAO();

        boolean respuesta = dao.editarIngrediente(ing);

        if(respuesta){

            JOptionPane.showMessageDialog(this,
                    "Ingrediente actualizado");

            dao.mostrarIngredientes(tblIngredientes);

        }else{

            JOptionPane.showMessageDialog(this,
                    "Error al actualizar");

        }
        limpiar();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void EliminarIngredienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarIngredienteActionPerformed
        // TODO add your handling code here:
        int opcion = JOptionPane.showConfirmDialog(
            this,
            "¿Deseas eliminar este ingrediente?",
            "Confirmar",
            JOptionPane.YES_NO_OPTION);

        if(opcion == JOptionPane.YES_OPTION){

            int id = Integer.parseInt(
                    txtIdIngrediente.getText());

            IngredienteDAO dao = new IngredienteDAO();

            boolean respuesta = dao.eliminarIngrediente(id);

            if(respuesta){

                JOptionPane.showMessageDialog(this,
                        "Empleado eliminado");

                dao.mostrarIngredientes(tblIngredientes);

            }else{

                JOptionPane.showMessageDialog(this,
                        "Error al eliminar");

            }

        }
        limpiar();
    }//GEN-LAST:event_EliminarIngredienteActionPerformed

