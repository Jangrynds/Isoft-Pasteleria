    private void tblIngredientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblIngredientesMouseClicked
        // TODO add your handling code here:
        int fila = tblIngredientes.getSelectedRow();

        if(fila >= 0){

            txtIdIngrediente.setText(
                tblIngredientes.getValueAt(fila, 0).toString()
            );

            txtNombreIngrediente.setText(
                tblIngredientes.getValueAt(fila, 1).toString()
            );

            txtCantidadIngrediente.setText(
              tblIngredientes.getValueAt(fila, 2).toString()
            );

            JcomboMedidasIngredientes.setSelectedItem(
                tblIngredientes.getValueAt(fila, 3).toString());
            }
            txtPrecioUnitario.setText(
                tblIngredientes.getValueAt(fila, 4).toString()
            );
    }//GEN-LAST:event_tblIngredientesMouseClicked

